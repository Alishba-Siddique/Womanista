  @override
  Widget build(BuildContext context) {
    double height = MediaQuery.of(context).size.height;
    double width = MediaQuery.of(context).size.width;
    return Expanded(
        child: Provider.of<ProductProvider>(context).products.isEmpty
            ? Column(
                mainAxisAlignment: MainAxisAlignment.center,
                children: [
                  SizedBox(
                    //height: height * 0.3,
                    //width: width,
                    child: Image.asset(
                      "assets/productsLoading.gif",
                      fit: BoxFit.fill,
                    ),
                  ),
                  Text(
                    "Loading...",
                    style: AppSettings.textStyle(size: 18),
                  ),
                ],
              )
            : GridView.builder(
                itemCount:
                    Provider.of<ProductProvider>(context).products.length,
                gridDelegate: const SliverGridDelegateWithFixedCrossAxisCount(
                    crossAxisCount: 2),
                itemBuilder: (context, index) {
                  final product =
                      context.read<ProductProvider>().products[index];
                  return InkWell(
                    onTap: () {
                      Navigator.of(context).push(MaterialPageRoute(
                          builder: (context) => ProductDetails(
                                product: product,
                              )));
                    },
                    child: Card(
                        child: Container(
                      padding: const EdgeInsets.all(8),
                      child: Column(
                        children: [
                          Expanded(
                            child: Image.network(
                              product.img!,
                              fit: BoxFit.contain,
                            ),
                          ),
                          Text(product.name!),
                          Text("\$ ${product.price!}"),
                        ],
                      ),
                    )),
                  );
                },
              ));
  }
